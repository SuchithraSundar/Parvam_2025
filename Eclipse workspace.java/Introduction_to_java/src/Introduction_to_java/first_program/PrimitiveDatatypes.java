package Introduction_to_java.first_program;

public class PrimitiveDatatypes {
	
	public static void main(String[] args) {
		//PrimitiveDatatypes
		/*
		 * 1. byte = 23 (default = 0) --> falsi values
		 * 2. short = 300 (default = 0)
		 * 3. int = 3000  (default = 0)
		 * 4. long = 300L (default = 0l)  
		 * 5. float = 8.9f (default = 0.0f)
		 * 6. double = 9.1 (default = 0.0)
		 * 7. char = 'a' (default = '')
		 * 8. boolean = true (default = false)
		 * */
		
		
		//declaration variable
		/*
		 * 
		 * variable - It is container used to store values based on  the data type
		 * <datatype> variable_name;
		 * */
		byte age; 
		short ages;
		int aged;
		long salary;
		float salary1;
		double salary2;
		char lname;
		boolean check;
		
		
		//initialization
		//variable_name =  value;
		age = 10;
		ages = 20;
		aged = 30;
		salary = 20000;
		salary1 = 30000f;
		salary2 = 30000;
		lname = 's';
		check = true;
		
		
		
		
		
		
		// <datatype> variable_name =  value;
		byte age1 = 30;
		
		
		System.out.println(age);
		System.out.println(age1);
		System.out.println(ages);
		System.out.println(aged);
		System.out.println(salary);
		System.out.println(salary1);
		System.out.println(salary2);
		System.out.println(lname);
		System.out.println(check);
		

	}

}
